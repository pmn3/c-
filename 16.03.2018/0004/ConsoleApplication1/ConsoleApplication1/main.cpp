#include <iostream>
	using namespace std;
	int main()
	{
		int i = 0;
		cout << "i = " << i << endl;
		cout << "i++ = " << i++ << endl;
		cout << "++i = " << ++i << endl;
		cout << "i+i = " << i + i << endl;
		cout << "i-- = " << i-- << endl;
		cout << "--i = " << --i << endl;
		cout << "i = " << i << endl;
		return 0;
	}


